//Function Normal
function somar (a, b) {
    return a + b
}

//Arrow function
const somarArrow = (a, b) => {
    return a + b
}

//Outra sintaxe de Arrow function
const somarArrow2 = (a, b) => a + b

//Exemplos
function verificarPositivo (numero) {
    return numero >= 0;
}

const verificarPositivoArrow = (numero) => numero >= 0;